# def funcao( parm1:tipo, parm2:tipo, parmn:tipo) -> tipo_retorno :
def cabcecao(texto: str, alinhamento: bool = True) -> str:
    if alinhamento:
        return f"{texto.title()}\n{'-' * len(texto)}"
    else:
        return f" {texto.title()} ".center(50, '#')


def cumpimentar(nome: str) -> str:
    return f'OLá {nome}, Seu Arromado !!! '


def main():
    print(cumpimentar('Pedrinho'))
    print(cabcecao('Univesidade Geka'))
    print(cabcecao('geek university', alinhamento=False))


## e é aqui que a brincadeitra começa
if __name__ == '__main__':
    main()
